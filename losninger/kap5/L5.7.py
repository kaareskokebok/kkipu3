# Leser inn to tall som desimaltall (float)
tallA = float(input("Oppgi et tall: "))
tallB = float(input("Oppgi et tall: "))

# Beregner sum, produkt og kvotient
summen = tallA + tallB
produkt = tallA * tallB
kvotient = tallA / tallB

# Skriver ut resultatene
print(f"{tallA} + {tallB} = {summen}")
print(f"{tallA} * {tallB} = {produkt}")
print(f"{tallA} / {tallB} = {kvotient}")